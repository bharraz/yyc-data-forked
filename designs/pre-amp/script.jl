#!/usr/bin/julia

calc_gain(r1, r2) = r2 / r1

function calc_params(r1, r2, r3, c1, c2)
    g = calc_gain(r1, r2)
    sqrtrc = √(r2 * r3 * c1 * c2)
    fc = 1 / 2 / π / sqrtrc
    q = sqrtrc / (r3 * c1 + r2 * c1 + r3 * c1 * g)
    return fc, q
end

function calc_reff(r′, r1)
    g = r′ / (r1 + r′)
    reff = g * r1
    return g, reff
end

function find_param(rs, cs, gcnd, fccnd, qcnd)
    for r1 in rs
        if r1 < 1
            continue
        elseif r1 > 20
            break
        end
        for r2 in rs
            g = calc_gain(r1, r2)
            gcnd(g) || continue
            for c1 in cs
                if c1 > 3000
                    break
                end
                for r3 in rs
                    for c2 in cs
                        fc, q = calc_params(r1, r2, r3, c1, c2)
                        if fccnd(fc) && qcnd(q)
                            println((r1, r2, r3, c1, c2), " -> ", (g, fc / 1.2736 * 1e6, q))
                        end
                    end
                end
            end
        end
    end
end

function find_param′(rs, cs, gcnd, fccnd, qcnd)
    for r′ in rs
        for r1′ in rs
            if r1′ < 1
                continue
            elseif r1′ > 10
                break
            elseif r′ > r1′ * 4
                continue
            elseif r′ < r1′ / 4
                break
            end
            g′, r1 = calc_reff(r′, r1′)
            for r2 in rs
                g = calc_gain(r1, r2) * g′
                gcnd(g) || continue
                for c1 in cs
                    if c1 > 2000
                        break
                    end
                    for r3 in rs
                        for c2 in cs
                            fc, q = calc_params(r1, r2, r3, c1, c2)
                            if fccnd(fc) && qcnd(q)
                                println((r′, r1′, r2, r3, c1, c2), " -> ",
                                        (g, fc / 1.2736 * 1e6, q))
                            end
                        end
                    end
                end
            end
        end
    end
end
